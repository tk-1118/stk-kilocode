/**
 * 任务分析工具描述
 * 帮助智能体进行任务分析和团队成员选择的指导
 */
export function getAnalyzeTaskDescription(): string {
	return `## analyze_task
Description: 智能体内部任务分析工具。在接收到新任务时，使用此工具进行深度分析，确定最合适的团队成员。这是一个思考工具，帮助您系统化地分析任务。

**使用时机：每个新任务开始时必须使用**

Parameters:
- task_description: (必需) 用户提供的任务描述
- analysis_depth: (可选) 分析深度 ("basic" | "detailed" | "comprehensive")

Usage:
<analyze_task>
<task_description>用户的任务描述</task_description>
<analysis_depth>detailed</analysis_depth>
</analyze_task>

**分析框架：**

1. **任务类型识别**
   - 是否涉及API接口开发？
   - 是否涉及数据库操作？
   - 是否涉及领域建模？
   - 是否涉及业务逻辑？
   - 是否涉及架构设计？

2. **技术领域判断**
   - 前端开发 (UI/UX, 组件, 页面)
   - 后端开发 (API, 服务, 数据库)
   - 全栈开发 (前后端结合)
   - 架构设计 (系统设计, 技术选型)
   - 问题排查 (调试, 故障分析)

3. **专业需求评估**
   - 需要什么专业知识？
   - 涉及哪些技术栈？
   - 复杂度如何？
   - 是否需要特定的最佳实践？

4. **团队成员匹配**
   - 当前团队有哪些专业成员？
   - 哪个成员最匹配任务需求？
   - 当前模式是否合适？
   - 是否需要切换？

**输出格式：**
- 任务类型：[具体类型]
- 技术领域：[具体领域]
- 推荐成员：[成员标识]
- 切换理由：[专业理由]
- 置信度：[高/中/低]

**重要提醒：**
- 这是智能体的内部分析工具
- 分析完成后必须基于结果采取行动
- 如需切换团队成员，立即使用 switch_mode 工具
- 展现您的专业判断和决策能力`
}
